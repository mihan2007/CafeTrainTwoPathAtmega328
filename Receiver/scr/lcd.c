void LCD_ClearLine(uint8_t row) {
	LCD_SetCursor(0, row);
	for (uint8_t i = 0; i < 16; i++) {
		LCD_Data(' ');
	}
	LCD_SetCursor(0, row);  // ????????? ? ?????? ??????
}

void LCD_SetCursor(uint8_t col, uint8_t row) {
	uint8_t row_offsets[] = {0x00, 0x40, 0x14, 0x54};
	LCD_Command(0x80 | (col + row_offsets[row])); // ????????? ?????? DDRAM
}

