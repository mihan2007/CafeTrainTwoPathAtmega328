void LCD_Send(uint8_t data, uint8_t mode) {
	uint8_t highNibble = (data & 0xF0) | LCD_BACKLIGHT | mode;
	uint8_t lowNibble = ((data << 4) & 0xF0) | LCD_BACKLIGHT | mode;

	I2C_Start();
	I2C_Write(LCD_ADDRESS << 1); // ????? ?????????? + ??? ??????
	I2C_Write(highNibble);       // ???????? ??????? 4 ???
	I2C_Write(highNibble | 0x04); // ????????? EN = 1
	I2C_Write(highNibble);       // ????? EN = 0
	I2C_Write(lowNibble);        // ???????? ??????? 4 ???
	I2C_Write(lowNibble | 0x04); // ????????? EN = 1
	I2C_Write(lowNibble);        // ????? EN = 0
	I2C_Stop();
}

